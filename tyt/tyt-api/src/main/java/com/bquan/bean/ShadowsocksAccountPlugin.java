package com.bquan.bean;

public class ShadowsocksAccountPlugin {

	private String id;
	private String type;
	private String userId;
	private String server;
	private String port;
	private String password;
	private String data;
	private String status;
	private String autoRemove;
	
	public ShadowsocksAccountPlugin(String id, String type, String userId, String server,String port, String password, String data,
			String status, String autoRemove) {
		this.id = id;
		this.type = type;
		this.userId = userId;
		this.server = server;
		this.port =port;
		this.password = password;
		this.data = data;
		this.status = status;
		this.autoRemove = autoRemove;
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getServer() {
		return server;
	}
	public void setServer(String server) {
		this.server = server;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getAutoRemove() {
		return autoRemove;
	}
	public void setAutoRemove(String autoRemove) {
		this.autoRemove = autoRemove;
	}

	public String getPort() {
		return port;
	}

	public void setPort(String port) {
		this.port = port;
	}
	
}
